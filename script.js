// Add water drop animation to hero section
function createWaterDrop() {
    const hero = document.querySelector('.hero');
    if (!hero) return;
    
    const drop = document.createElement('div');
    drop.className = 'water-drop-animation';
    drop.style.cssText = `
        position: absolute;
        width: 4px;
        height: 4px;
        background: rgba(255, 255, 255, 0.6);
        border-radius: 50%;
        pointer-events: none;
        animation: waterDropFall 3s linear infinite;
    `;
    
    drop.style.left = Math.random() * 100 + '%';
    drop.style.animationDelay = Math.random() * 2 + 's';
    
    hero.appendChild(drop);
    
    setTimeout(() => {
        drop.remove();
    }, 3000);
}

// Add CSS for water drop animation
const style = document.createElement('style');
style.textContent = `
    @keyframes waterDropFall {
        0% {
            transform: translateY(-100vh);
            opacity: 1;
        }
        100% {
            transform: translateY(100vh);
            opacity: 0;
        }
    }
`;
document.head.appendChild(style);

// Create water drops periodically
setInterval(createWaterDrop, 500);

// Add section highlighting on scroll
const sections = document.querySelectorAll('section[id]');
const navLinks = document.querySelectorAll('.nav-link');

window.addEventListener('scroll', () => {
    let current = '';
    
    sections.forEach(section => {
        const sectionTop = section.offsetTop;
        const sectionHeight = section.clientHeight;
        
        if (window.pageYOffset >= sectionTop - 200) {
            current = section.getAttribute('id');
        }
    });
    
    navLinks.forEach(link => {
        link.classList.remove('active');
        if (link.getAttribute('href') === `#${current}`) {
            link.classList.add('active');
        }
    });
});

